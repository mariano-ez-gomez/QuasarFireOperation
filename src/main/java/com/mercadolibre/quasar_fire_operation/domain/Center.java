package com.mercadolibre.quasar_fire_operation.domain;

public class Center {

    private float x;
    private float y;

    public Center(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

}
